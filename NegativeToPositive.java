package week1.assignment;

public class NegativeToPositive {

	public static void main(String[] args) {
		int i=-40;
		int a=-(i);
		if(a>0)
		System.out.println(i + " is converted to positive number " + a);
		else
		System.out.println(a + " is negative number");
	}

}

